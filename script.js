// Quiz Functions
function loadQuestion() {
    const quizCard = document.querySelector('.quiz-card');
    if(!quizCard) return;
    
    const questionObj = quizData[currentQuestion];
    quizCard.innerHTML = `
        <div class="question">
            <p>${currentQuestion + 1}. ${questionObj.question}</p>
        </div>
        <div class="options">
            ${questionObj.options.map((option, index) => `
                <button class="option" onclick="checkAnswer(${index})">${option}</button>
            `).join('')}
        </div>
        <div class="progress">рдкреНрд░рд╢реНрди ${currentQuestion + 1}/${quizData.length}</div>
        <div class="score">рд╕реНрдХреЛрд░: ${score}</div>
    `;
}

function checkAnswer(selectedIndex) {
    const correctIndex = quizData[currentQuestion].correct;
    const options = document.querySelectorAll('.option');
    
    options.forEach(option => option.disabled = true);
    
    if(selectedIndex === correctIndex) {
        options[selectedIndex].style.backgroundColor = '#2ecc71';
        score++;
    } else {
        options[selectedIndex].style.backgroundColor = '#e74c3c';
        options[correctIndex].style.backgroundColor = '#2ecc71';
    }

    setTimeout(() => {
        currentQuestion++;
        currentQuestion < quizData.length ? loadQuestion() : showFinalResults();
    }, 1500);
}

function showFinalResults() {
    const quizCard = document.querySelector('.quiz-card');
    if(!quizCard) return;
    
    quizCard.innerHTML = `
        <h3>рдХреНрд╡рд┐рдЬрд╝ рдкрд░рд┐рдгрд╛рдо</h3>
        <p>рдЖрдкрдХрд╛ рд╕реНрдХреЛрд░: ${score}/${quizData.length}</p>
        <button onclick="resetQuiz()">рдлрд┐рд░ рд╕реЗ рдЦреЗрд▓реЗрдВ</button>
    `;
}

function resetQuiz() {
    currentQuestion = 0;
    score = 0;
    loadQuestion();
}

// Fact Functions
function loadFacts() {
    const factGrid = document.querySelector('.fact-grid');
    if(!factGrid) return;
    
    factGrid.innerHTML = factCategories.map(fact => `
        <div class="fact-card">
            <h3>${fact.category}</h3>
            <p>${fact.content}</p>
        </div>
    `).join('');
}

function showDailyFact() {
    const today = new Date().getDate();
    const factIndex = today % dailyFacts.length;
    const dailyFactContainer = document.getElementById('daily-fact');
    
    if(dailyFactContainer) {
        dailyFactContainer.innerHTML = `
            <div class="fact-of-day">
                <h3>ЁЯУЖ рджрд┐рди рдХрд╛ рддрдереНрдп</h3>
                <p>${dailyFacts[factIndex]}</p>
            </div>
        `;
    }
}

// Common Functions
function initializeAnimations() {
    const factCards = document.querySelectorAll('.fact-card');
    factCards.forEach(card => {
        card.style.transform = 'translateY(20px)';
        card.style.opacity = '0';
        card.style.transition = 'all 0.5s ease';
    });

    setTimeout(() => {
        factCards.forEach(card => {
            card.style.transform = 'translateY(0)';
            card.style.opacity = '1';
        });
    }, 500);
}

function handleScroll() {
    const scrollBtn = document.querySelector('.scroll-top');
    if(window.scrollY > 300) {
        scrollBtn.style.display = 'block';
    } else {
        scrollBtn.style.display = 'none';
    }
}

function scrollToTop() {
    window.scrollTo({
        top: 0,
        behavior: 'smooth'
    });
}

// Initialize App
window.onload = function() {
    // Common Initialization
    document.body.innerHTML += `<button class="scroll-top" onclick="scrollToTop()">тЖС</button>`;
    window.addEventListener('scroll', handleScroll);
    
    // Page Specific Initialization
    loadQuestion();
    loadFacts();
    showDailyFact();
    initializeAnimations();
};

